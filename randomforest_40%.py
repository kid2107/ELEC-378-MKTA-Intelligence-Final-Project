def plot_feature_importance(rf, top_n=30, img_size=224):
    """Plot the top-N most important pixels (useful for the report)."""
    importances = rf.feature_importances_        # shape: (224*224*3,)
    top_idx     = np.argsort(importances)[::-1][:top_n]

    plt.figure(figsize=(12, 4))
    plt.bar(range(top_n), importances[top_idx])
    plt.xlabel("Pixel index (ranked)")
    plt.ylabel("Importance")
    plt.title(f"Top {top_n} pixel importances – Random Forest")
    plt.tight_layout()
    plt.savefig("rf_feature_importance.png", dpi=150)
    plt.show()
    print("Saved rf_feature_importance.png")

