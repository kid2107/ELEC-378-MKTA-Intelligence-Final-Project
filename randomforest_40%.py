def load_image(path, size=64):
    return np.array(Image.open(path).convert("RGB").resize((size, size)))

def imgToFeatureMatrix(imgPath, imgFiles, labelList, count=0, step=0):
    """Flatten pixel data into a 2-D feature matrix.
    Identical interface to the version in skeleton.py so the two scripts
    are directly comparable.
    """
    n = len(imgFiles)
    if count == 0:
        count = n
    if step == 0:
        step = max(1, n // count)

    features, labels = [], []
    for i in range(0, n, step):
        if len(features) >= count:
            break
        path = os.path.join(imgPath, imgFiles[i])
        img  = load_image(path, size=64)
        features.append(img.flatten() / 255.0)
        labels.append(labelList[i])

    return np.array(labels), np.array(features)

def train_random_forest(X_train, X_val, y_train, y_val, le,
                        n_estimators=200, max_depth=None,
                        min_samples_leaf=2, max_features="sqrt"):
    y_train_enc = le.transform(np.ravel(y_train))
    y_val_enc   = le.transform(np.ravel(y_val))

    print(f"Training Random Forest  |  trees={n_estimators}, "
          f"max_depth={max_depth}, min_samples_leaf={min_samples_leaf}, "
          f"max_features={max_features}")

    rf = RandomForestClassifier(
        n_estimators=n_estimators,
        max_depth=max_depth,
        min_samples_leaf=min_samples_leaf,
        max_features=max_features,
        n_jobs=-1,
        random_state=RANDOM_STATE,
        class_weight="balanced",
    )
    rf.fit(X_train, y_train_enc)

    val_preds = rf.predict(X_val)
    val_acc   = accuracy_score(y_val_enc, val_preds)
    print(f"Validation accuracy: {val_acc:.4f}\n")
    print(classification_report(y_val_enc, val_preds, zero_division=0))

    return rf, le

